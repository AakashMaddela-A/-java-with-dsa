package basics;

public class intByint {
    public static void main(String[] args) {
        //int by int
        System.out.println(5/2);

        //int by double
        System.out.println(5/2.0);

        //double by double
        System.out.println(5.0/2.0);

        //double by int
        System.out.println(5.0/2);

    }
}

//ouput
//2
//2.5
//2.5
//2.5
