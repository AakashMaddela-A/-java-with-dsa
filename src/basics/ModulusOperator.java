package basics;

public class ModulusOperator {
    public static void main(String[] args) {
        int a=8;
        int b=3;
        System.out.println(a%b);
        System.out.println(4%3);
        System.out.println(3%4);
        System.out.println(-10%3);
        System.out.println(10%-3);
        System.out.println(-10%-3);
    }
}

//output
//2
//1
//3
//-1
//1
//-1