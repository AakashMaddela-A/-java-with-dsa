package Arrays;

public class basicSyntax {
    public static void main(String[] args) {
        int arr[]=new int[5];
        //initializing individual elements
        arr[0]=10;
        arr[1]=20;
        arr[2]=30;
        arr[3]=40;
        arr[4]=50;
        //printing
        System.out.println(arr[0]);
        //updating
        arr[0]=90;
        System.out.println(arr[0]);
    }
}
